# Variables
function tpl_vars($cmd, $urlParams=array()){
	global $vistas;
	$cmd = strtoupper(enArray($cmd,$vistas));
	if($cmd == 'INDEX'){
		$vars = vars_index($cmd, $urlParams);
	}elseif($cmd == 'USUARIOS'){
		$vars = vars_usuarios($cmd, $urlParams);
	}else{
		$vars = vars_error($cmd);
	}
	return $vars;
}

